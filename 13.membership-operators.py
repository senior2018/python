#Membership operator
#Include in and not in
#Used to check a membership in a sequence
#used to check if a character is there in a string

str = 'Samwel'

print('y' in str) #Return false

print('a' in str) #Return true

print('s' in str) #Return false

print('P' not in str) #Return true

#Incase of list

a = [1, 3, 34, 5, 6, 7, 'a']

print(1 in a) #Return true

print('a' not in a) #Return False